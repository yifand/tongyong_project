CREATE TABLE IF NOT EXISTS site (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    site_code VARCHAR(255),
    site_name VARCHAR(255),
    created_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS edge_box (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    box_id VARCHAR(255),
    box_name VARCHAR(255),
    site_id BIGINT,
    ip_address VARCHAR(255),
    secret_key VARCHAR(255),
    status INT,
    last_heartbeat TIMESTAMP,
    version VARCHAR(255),
    cpu_usage DECIMAL(19,2),
    mem_usage DECIMAL(19,2),
    disk_usage DECIMAL(19,2),
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS channel (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    channel_id VARCHAR(255),
    channel_name VARCHAR(255),
    box_id BIGINT,
    channel_type VARCHAR(255),
    status INT,
    algorithm_type VARCHAR(255),
    rtsp_url VARCHAR(255),
    username VARCHAR(255),
    password VARCHAR(255),
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS rule_config (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rule_name VARCHAR(255),
    channel_type VARCHAR(255),
    require_vehicle BOOLEAN,
    enter_pattern VARCHAR(4000),
    exit_pattern VARCHAR(4000),
    standard_duration INT,
    critical_threshold_pct DECIMAL(19,2),
    person_absent_timeout INT,
    is_enabled BOOLEAN,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS work_session (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    site_id BIGINT,
    channel_id BIGINT,
    vehicle_info VARCHAR(255),
    start_time TIMESTAMP,
    end_time TIMESTAMP,
    actual_duration INT,
    standard_duration INT,
    deviation_pct DECIMAL(19,2),
    result VARCHAR(255),
    snapshot_head VARCHAR(255),
    snapshot_tail VARCHAR(255),
    snapshot_mid VARCHAR(255),
    status INT,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS alarm (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alarm_type VARCHAR(255),
    site_id BIGINT,
    channel_id BIGINT,
    work_session_id BIGINT,
    alarm_time TIMESTAMP,
    process_status VARCHAR(255),
    processed_by BIGINT,
    processed_at TIMESTAMP,
    target_image VARCHAR(255),
    scene_image VARCHAR(255),
    watermark_logo VARCHAR(255),
    description VARCHAR(255),
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS sys_role (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_code VARCHAR(255),
    role_name VARCHAR(255),
    permissions VARCHAR(4000),
    data_scope VARCHAR(255),
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS sys_user (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(255),
    password_hash VARCHAR(255),
    real_name VARCHAR(255),
    phone VARCHAR(255),
    email VARCHAR(255),
    role_id BIGINT,
    site_id BIGINT,
    status INT,
    last_login_at TIMESTAMP,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS operation_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT,
    username VARCHAR(255),
    ip_address VARCHAR(255),
    operation_type VARCHAR(255),
    operation_content VARCHAR(255),
    result INT,
    created_at TIMESTAMP
);
